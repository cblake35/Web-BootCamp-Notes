/* SWITCH STATEMENTS */

let friend = "Kenn";

switch(friend){
    case "Tom":
        console.log("Hey Tom, when will you bring sourdough bread for me?");
        break;
    case "Kenn":
        console.log("Hey Kenn, how do you own a wold in your backyard?");
        break;
    case "Alex":
        console.log("Hey Alex, how many magic cards do you own");
        break;
    default:
        console.log(`I'm sorry, ${friend}, how do I know you?`);
}

